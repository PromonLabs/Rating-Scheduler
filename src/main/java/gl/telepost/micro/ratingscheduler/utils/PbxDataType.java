package gl.telepost.micro.ratingscheduler.utils;

public class PbxDataType
{
    protected String accountId;
    protected String hostedPBX;
    protected String name;
    protected String totalNumbers;
    protected String domain;
    protected Integer userLimit;
    protected Short accountExternalIdType;
    protected String accountExternalId;
    protected String companyName;
    protected String mplsId;
    protected Integer totalUserLimit;
    protected String groupID;

    public String getAccountId() {
        return accountId;
    }

    public void setAccountId(String accountId) {
        this.accountId = accountId;
    }

    public String getHostedPBX() {
        return hostedPBX;
    }

    public void setHostedPBX(String hostedPBX) {
        this.hostedPBX = hostedPBX;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTotalNumbers() {
        return totalNumbers;
    }

    public void setTotalNumbers(String totalNumbers) {
        this.totalNumbers = totalNumbers;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public Integer getUserLimit() {
        return userLimit;
    }

    public void setUserLimit(Integer userLimit) {
        this.userLimit = userLimit;
    }

    public Short getAccountExternalIdType() {
        return accountExternalIdType;
    }

    public void setAccountExternalIdType(Short accountExternalIdType) {
        this.accountExternalIdType = accountExternalIdType;
    }

    public String getAccountExternalId() {
        return accountExternalId;
    }

    public void setAccountExternalId(String accountExternalId) {
        this.accountExternalId = accountExternalId;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getMplsId() {
        return mplsId;
    }

    public void setMplsId(String mplsId) {
        this.mplsId = mplsId;
    }

    public Integer getTotalUserLimit() {
        return totalUserLimit;
    }

    public void setTotalUserLimit(Integer totalUserLimit) {
        this.totalUserLimit = totalUserLimit;
    }

    public String getGroupID() {
        return groupID;
    }

    public void setGroupID(String groupID) {
        this.groupID = groupID;
    }
}
